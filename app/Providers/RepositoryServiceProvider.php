<?php

namespace App\Providers;

use Illuminate\Support\ServiceProvider;

class RepositoryServiceProvider extends ServiceProvider
{
    /**
     * Bootstrap services.
     *
     * @return void
     */
    public function boot()
    {
        //
    }

    /**
     * Register services.
     *
     * @return void
     */
    public function register()
    {
        $this->app->bind(
            \Fighter\Identity\Domain\Models\User\UserCommandRepositoryInterface::class,
            \Fighter\Identity\Infrastructure\Repositories\User\UserCommandRepository::class
        );
    }
}
